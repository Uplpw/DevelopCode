public class TB_UserInfo {
	private String username;
	private String petname;
	private String userpicture;
	private String usertelephone;
	private String userreal;
	private long usercredit;
	/**
	 * @return the username
	 */
	public String getUsername() {
		return username;
	}
	/**
	 * @param username the username to set
	 */
	public void setUsername(String username) {
		this.username = username;
	}
	/**
	 * @return the petname
	 */
	public String getPetname() {
		return petname;
	}
	/**
	 * @param petname the petname to set
	 */
	public void setPetname(String petname) {
		this.petname = petname;
	}
	/**
	 * @return the userpicture
	 */
	public String getUserpicture() {
		return userpicture;
	}
	/**
	 * @param userpicture the userpicture to set
	 */
	public void setUserpicture(String userpicture) {
		this.userpicture = userpicture;
	}
	/**
	 * @return the usertelephone
	 */
	public String getUsertelephone() {
		return usertelephone;
	}
	/**
	 * @param usertelephone the usertelephone to set
	 */
	public void setUsertelephone(String usertelephone) {
		this.usertelephone = usertelephone;
	}
	/**
	 * @return the userreal
	 */
	public String getUserreal() {
		return userreal;
	}
	/**
	 * @param userreal the userreal to set
	 */
	public void setUserreal(String userreal) {
		this.userreal = userreal;
	}
	/**
	 * @return the usercredit
	 */
	public long getUsercredit() {
		return usercredit;
	}
	/**
	 * @param usercredit the usercredit to set
	 */
	public void setUsercredit(long usercredit) {
		this.usercredit = usercredit;
	}
	
}
